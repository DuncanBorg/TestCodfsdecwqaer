package Chat;

public class Users {
	public String username;
	public String password;
	public int id;
	public String Provider;
	public String Status;
	
	public Users(String u , String p , int i , String prov , String s)
	{
		username = u;
		password = p;
		id = i;
		Provider = prov;
		Status = s;
	}
	
}
